export function getCityActivities(cityName = '') {
  return CITY_ACTIVITIES[cityName] || null;
}

export function getActivitiesByCategory(cityName = '', category = 'culture') {
  return CITY_ACTIVITIES[cityName]?.[category] || [];
}

export function getActivityPool(cityName = '', categories = []) {
  const cityActivities = getCityActivities(cityName);

  if (!cityActivities) return [];

  if (!categories.length) {
    return Object.values(cityActivities).flat();
  }

  return categories.flatMap((category) => cityActivities[category] || []);
}

export function pickActivity(cityName = '', category = 'culture', index = 0) {
  const activities = getActivitiesByCategory(cityName, category);

  if (!activities.length) return null;

  return activities[index % activities.length];
}

export function getRainActivities(cityName = '') {
  return getActivitiesByCategory(cityName, 'rain');
}
